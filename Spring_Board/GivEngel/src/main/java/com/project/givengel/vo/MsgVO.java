package com.project.givengel.vo;

public class MsgVO {
	private int msg_no;
	private String msg_to;
	private String msg_from;
	private String msg_detail;
	private boolean msg_checked;
	private String msg_date;
	private String msg_where;
	
	
	
	public int getMsg_no() {
		return msg_no;
	}
	public void setMsg_no(int msg_no) {
		this.msg_no = msg_no;
	}
	public String getMsg_to() {
		return msg_to;
	}
	public void setMsg_to(String msg_to) {
		this.msg_to = msg_to;
	}
	public String getMsg_from() {
		return msg_from;
	}
	public void setMsg_from(String msg_from) {
		this.msg_from = msg_from;
	}
	public String getMsg_detail() {
		return msg_detail;
	}
	public void setMsg_detail(String msg_detail) {
		this.msg_detail = msg_detail;
	}
	public boolean isMsg_checked() {
		return msg_checked;
	}
	public void setMsg_checked(boolean msg_checked) {
		this.msg_checked = msg_checked;
	}
	public String getMsg_date() {
		return msg_date;
	}
	public void setMsg_date(String msg_date) {
		this.msg_date = msg_date;
	}
	public String getMsg_where() {
		return msg_where;
	}
	public void setMsg_where(String msg_where) {
		this.msg_where = msg_where;
	}
}
