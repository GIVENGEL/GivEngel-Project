package com.project.givengel.vo;

public class AdminVO {
	
	private int admin_no;
	private String admin_id;
	private String admin_pw;
	private String admin_lastdate;
	private int admin_level;
	private boolean admin_available;
	
	public int getAdmin_no() {
		return admin_no;
	}
	public void setAdmin_no(int admin_no) {
		this.admin_no = admin_no;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getAdmin_pw() {
		return admin_pw;
	}
	public void setAdmin_pw(String admin_pw) {
		this.admin_pw = admin_pw;
	}
	public String getAdmin_lastdate() {
		return admin_lastdate;
	}
	public void setAdmin_lastdate(String admin_lastdate) {
		this.admin_lastdate = admin_lastdate;
	}
	public int getAdmin_level() {
		return admin_level;
	}
	public void setAdmin_level(int admin_level) {
		this.admin_level = admin_level;
	}
	public boolean isAdmin_available() {
		return admin_available;
	}
	public void setAdmin_available(boolean admin_available) {
		this.admin_available = admin_available;
	}
	
	
	
	

}
